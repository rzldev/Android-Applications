package com.example.recyclerviewadvanced;

public class Mantan {
    String nama, kenangan;
    int lama;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKenangan() {
        return kenangan;
    }

    public void setKenangan(String kenangan) {
        this.kenangan = kenangan;
    }

    public int getLama() {
        return lama;
    }

    public void setLama(int lama) {
        this.lama = lama;
    }
}
